import UIKit
import CoreData
import Firebase

@UIApplicationMain
class AppDelegate: UIResponder, UIApplicationDelegate {

    var window: UIWindow?
    
    // DataController property
    let dataController = DataController(modelName: "TimeTracker")
    

    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplicationLaunchOptionsKey: Any]?) -> Bool {
        // connect Firebase 
        FirebaseApp.configure()
        
        let connectedRef = Database.database().reference(withPath: ".info/connected")
        connectedRef.observe(.value, with: { snapshot in
            if snapshot.value as? Bool ?? false {
                print("Connected")
            } else {
                print("Not connected")
            }
        })
        
        // load the persistent store
        dataController.load()

        let navigationController = window?.rootViewController as! UINavigationController
        let InitialViewController = navigationController.topViewController as! InitialViewController
        InitialViewController.dataController = dataController
        // Override point for customization after application launch.
        return true
    }

